package Utilis;

public class Inform {
    public String nombre;
    public String apellido;
    private String id;
    private String telefono;
    private String direccion;
    private int edad;

    public Inform(String nombre, String apellido, String id, String telefono, String direccion, int edad){
        this.nombre = nombre;
        this.apellido = apellido;
        this.id = id;
        this.telefono = telefono;
        this.direccion = direccion;
        this.edad = edad;
    }
}
